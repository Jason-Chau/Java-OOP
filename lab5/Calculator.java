    public void arithmeticOperations(){
        switch(operator){
            case 1:{
                answer = number + Double.parseDouble(IntegerField.getText());
                IntegerField.setText(Double.toString(answer));
                break;
            }
            case 2:{
                answer = number - Double.parseDouble(IntegerField.getText());
                IntegerField.setText(Double.toString(answer));
                break;
            }
            case 3:{
                answer = number * Double.parseDouble(IntegerField.getText());
                IntegerField.setText(Double.toString(answer));
                break;
            }
            case 4:{
                answer = number / Double.parseDouble(IntegerField.getText());
                IntegerField.setText(Double.toString(answer));
                break;
            }
        }
    }
