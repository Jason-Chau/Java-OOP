    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int length = IntegerField.getText().length();
        int number = IntegerField.getText().length() - 1;
        String deletedNumber;
        if(length > 0){
            StringBuilder delete = new StringBuilder(IntegerField.getText());
            delete.deleteCharAt(number);
            deletedNumber = delete.toString();
            IntegerField.setText(deletedNumber);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

